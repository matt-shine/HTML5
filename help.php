						<h3>2. Using the Service</h3>
						<details>
							<summary>
								<b>2a. URL Validation and Direct code Input</b>
							</summary>
							<p>
								Users may use the HTML5 Learning Tool to validate the code of existing websites by using the URL validation feature. This feature allows users to enter a URL into the area provided which will validate the website at the URL.
							</p>
							<p>
								Users may also input HTML5 code directly to the tool for validation. This can be achieved by simply pasting any code into the space provided and clicking "Check".
							</p>
						</details>
						<details>
							<summary>
								<b>2b. File Upload</b>
							</summary>
							<p>
								Users can upload files onto the HTML5 Learning Tool for validation. This feature allows users to select any HTML files and upload it onto the tool to be validated. The code on the files are displayed and any errors or warnings are displayed to the user.
							</p>
							<p>
								Users can also choose to upload an entire website directory as a zip file. This will validate all the files on the directory and users will be able to view the code and errors on each file.
							</p>
						</details>
						<details>
							<summary>
								<b>2c. Error Result Page</b>
							</summary>
							<p>
								After submitting code to be validated through any of the above methods, users are greeted by the Results page. Here, users are able to view their source code and any errors or warnings detected within the code.
							</p>
							<p>
								Errors and warnings are colour-coded to allow users to quickly and easily identify problem areas.
							</p>
						</details>	
